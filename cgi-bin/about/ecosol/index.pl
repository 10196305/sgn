<p>The ECO-SOL, formerly called the SOL-ANDINO project, is an international and integrative research network for scientists to share and enrich resources, visions and capacities relating to biodiversity, ecology, conservation, breeding and sustainable agriculture of Solanaceae in the centers of their biodiversity. This initiative of the SOL network provides a virtual umbrella to link the tremendous Sol genomic efforts to basic and applied biological research of Solanaceae in their native and agricultural habitats. ECO-SOL will facilitate and coordinate networks between researchers and funding agencies to achieve a multidisciplinary expedition into the study of biodiversity and adaptation and by supporting the creation of permanent in situ conservation sites in the centers of Solanaceae biodiversity, such as the Andean region of South America. The initiative will integrate expertise, visions and perspectives of scientists from different biological and agricultural research fields and nationalities into the SOL Network. Currently, the ECO-SOL initiative anticipates connecting scientists through three major objectives: 
</p>
<ol>
<li>mapping, conserving and characterizing Solanaceae biodiversity, </li>
<li>advancing natural history and ecology research of Solanaceae, and </li>
<li>improving the efficiency and sustainability of Solanaceae breeding and cultivation. </li>
</ol>
<p>A <a href="/documents/about/ecosol/ECO-SOL_whitepaper_20060805.pdf">whitepaper</a> is available with more details about the current ECO-SOL objectives. It includes contributions from Solanaceae scientist around the world and represents a general framework that is subject to more specific development. The content of the whitepaper can be freely used by people who want to write grants within the ECO-SOL framework.</p>

<p>Building for the future of plant science depends upon the development of an integrated, collaborative and diverse research community. The ECO-SOL network will help to provide the framework for the establishment of a shared vision that will enhance both biological and cultural diversity of the SOL community. Partnerships forged across biological disciplines that have traditionally been separated and integrating our knowledge of plants across a broad scientific spectrum have the real potential to take plant science to a new and promising level.</p>

