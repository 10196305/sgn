    <p dir="ltr" style="text-align: left"><span style=
    "font-weight: bold">Research highlights</span></p>

    <p dir="ltr" style="text-align: left; text-indent: 0.5000in">
    The focus of my laboratory is the development of molecular
    tools for the improvement of vegetable crops, in particular
    pepper (<span style="font-style: italic">Capsicum</span>
    spp.).</p>

    <p dir="ltr" style="text-align: left; text-indent: 0.5000in">
    Our first objective is the development of breeding lines
    containing disease resistance genes and a concomitant
    identification of molecular markers linked to these genes for
    use in marker assisted selection. This work has been focused on
    resistances to viruses such as TMV, TSWV (Jahn et al. 2000) and
    CMV (Ben Chaim et al. 2001a) and to fungal diseases such as
    powdery mildew caused by <span style=
    "font-style: italic">Leveillula taurica</span>.</p>

    <p dir="ltr" style="text-align: left; text-indent: 0.5000in">
    Our second objective is the identification of the major genes
    controlling fruit yield and quality parameters in pepper. We
    associated the genes capsanthin capsorubin synthase and
    phytoene synthase with mature fruit color changes (Popovsky and
    Paran, 2000; Thorup et al. 2000). We identified a major
    domestication gene, fruit polygalacturonase that is associated
    with the soft flesh and deciduous fruit characters present in
    wild <span style="font-style: italic">Capsicum</span> species
    (Rao et al. 2003a). We mapped the <span style=
    "font-style: italic">C</span> locus that is required for the
    production of pungent fruit (Blum et al. 2002) and identified a
    major quantitative trait locus (QTL) affecting the level of
    pungency (Blum et al. 2003).</p>

    <p dir="ltr" style="text-align: left; text-indent: 0.5000in">We
    identified QTLs for major fruit quantitative traits such as
    fruit weight, fruit shape, pericarp thickness and maturity (Ben
    Chaim et al. 2001b, Ben Chaim et al. 2003a, Ben Chaim et al.
    2003b, Rao et al. 2003b). The pepper QTL data allow us to
    determine the degree of conservation of QTLs in <span style=
    "font-style: italic">Capsicum</span> and among this genus and
    other members of the Solanaceae family.</p>

